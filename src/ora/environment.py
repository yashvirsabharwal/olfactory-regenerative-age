def pip_freeze() -> str:
    """Return the current pip freeze output."""

    result = subprocess.run(
        [sys.executable, "-m", "pip", "freeze", "--all"],
        check=False,
        capture_output=True,
        text=True,
    )
    return result.stdout


def _python_smoke_row(test: str, specs: tuple[PackageSpec, ...]) -> dict[str, str]:
    missing = []
    for spec in specs:
        module_name = spec.module or spec.package.replace("-", "_")
        try:
            __import__(module_name)
        except Exception as exc:  # pragma: no cover - exercised by environment state
            missing.append(f"{module_name}: {exc.__class__.__name__}")
    return {
        "test": test,
        "status": "pass" if not missing else "fail",
        "detail": "all imports succeeded" if not missing else "; ".join(missing),
    }


def _r_smoke_row() -> dict[str, str]:
    rscript = shutil.which("Rscript")
    if not rscript:
        return {"test": "r_bioconductor", "status": "blocked", "detail": "Rscript not installed locally"}
    expression = "; ".join(f"suppressPackageStartupMessages(library({package}))" for package in R_PACKAGES)
    result = subprocess.run(
        [rscript, "-e", f"{expression}; cat('ok\\n')"],
        check=False,
        capture_output=True,
        text=True,
    )
    if result.returncode == 0:
        return {"test": "r_bioconductor", "status": "pass", "detail": "all R packages loaded"}
    detail = (result.stderr or result.stdout).strip().replace("\n", " ")
    return {"test": "r_bioconductor", "status": "fail", "detail": detail[:300]}


def _torch_cuda_available() -> str:
    try:
        import torch

        return str(bool(torch.cuda.is_available()))
    except Exception:
        return ""


def _command_text(command: list[str]) -> str:
    try:
        result = subprocess.run(command, check=False, capture_output=True, text=True)
    except FileNotFoundError:
        return ""
    text = (result.stdout or result.stderr).strip()
    return text.replace("\n", " ")

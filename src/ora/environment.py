def package_rows(
    specs: tuple[PackageSpec, ...],
    *,
    group: str,
    version_lookup: Callable[[str], str | None] | None = None,
) -> list[dict[str, str]]:
    """Return package/version rows for a group."""

    lookup = version_lookup or installed_version
    rows = []
    for spec in specs:
        version = lookup(spec.package)
        rows.append(
            {
                "group": group,
                "package": spec.package,
                "module": spec.module or spec.package.replace("-", "_"),
                "version": version or "",
                "status": "present" if version else "missing",
            }
        )
    return rows


def installed_version(package: str) -> str | None:
    """Return an installed package version, if available."""

    try:
        return importlib_metadata.version(package)
    except importlib_metadata.PackageNotFoundError:
        return None


def lockfile_text(rows: list[dict[str, str]], *, title: str) -> str:
    """Render package rows as a pip-compatible direct lock file."""

    lines = [
        f"# {title}",
        f"# Python: {platform.python_version()}",
        "# Generated by scripts/build_environment_report.py",
        "",
    ]
    for row in sorted(rows, key=lambda item: item["package"].lower()):
        package = row["package"]
        version = row["version"]
        if version:
            lines.append(f"{package}=={version}")
        else:
            lines.append(f"# MISSING: {package}")
    return "\n".join(lines) + "\n"


def runtime_rows() -> list[dict[str, str]]:
    """Collect local runtime metadata for the environment report."""

    return [
        {"key": "platform", "value": platform.platform()},
        {"key": "machine", "value": platform.machine()},
        {"key": "processor", "value": platform.processor()},
        {"key": "python_version", "value": platform.python_version()},
        {"key": "python_executable", "value": sys.executable},
        {"key": "pip_version", "value": _command_text([sys.executable, "-m", "pip", "--version"])},
        {"key": "rscript_path", "value": shutil.which("Rscript") or ""},
        {"key": "rscript_version", "value": _command_text(["Rscript", "--version"]) if shutil.which("Rscript") else ""},
        {"key": "torch_cuda_available", "value": _torch_cuda_available()},
    ]


def smoke_test_rows() -> list[dict[str, str]]:
    """Run lightweight import/package smoke checks."""

    rows = []
    rows.append(_python_smoke_row("python_base", BASE_PACKAGES))
    rows.append(_python_smoke_row("python_latent", LATENT_PACKAGES))
    rows.append(_r_smoke_row())
    return rows


def render_environment_markdown(
    runtime: pd.DataFrame,
    packages: pd.DataFrame,
    smoke_tests: pd.DataFrame,
) -> str:
    """Render a compact human-readable environment report."""

    lines = [
        "# Environment Report",
        "",
        "Generated by `make environment-report`.",
        "",
        "## Runtime",
        "",
        "| Key | Value |",
        "| --- | --- |",
    ]
    for _, row in runtime.iterrows():
        lines.append(f"| {row['key']} | `{row['value']}` |")

    lines.extend(["", "## Package Groups", "", "| Group | Present | Missing |", "| --- | ---: | ---: |"])
    group_counts = packages.groupby(["group", "status"], dropna=False).size().unstack(fill_value=0)
    for group, row in group_counts.iterrows():
        lines.append(f"| {group} | {int(row.get('present', 0))} | {int(row.get('missing', 0))} |")

    lines.extend(["", "## Smoke Tests", "", "| Test | Status | Detail |", "| --- | --- | --- |"])
    for _, row in smoke_tests.iterrows():
        lines.append(f"| {row['test']} | {row['status']} | {row['detail']} |")
    lines.append("")
    return "\n".join(lines)


